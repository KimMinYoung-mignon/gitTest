    function form_check(){
        var u_name = document.getElementById("u_name");
        var u_id = document.getElementById("u_id");
        var u_pwd = document.getElementById("u_pwd");
        var re_pwd = document.getElementById("re_pwd");
        var agree = document.getElementById("agree");

        if (!u_name.value) {
            alert("이름을 입력하세요.");
            u_name.focus();
            return false;
        };

        if (!u_id.value){
            alert("아이디을 입력하세요.");
            u_name.focus();
            return false;
        };

        if (u_id.value.length < 4 || u_id.value.length > 12){
            alert("아이디는 4자 ~ 12자 이내로 입력해주세요.");
            u_id.focus();
            return false;
        };

        if(!u_pwd.value){
            alert("비밀번호를 입력해주세요.");
            u_pwd.focus();
            return false;
        };

            function chkPW(){
            var pw = $("#u_pwd").val();
            var num = pw.search(/[0-9]/g);
            var eng = pw.search(/[a-z]/ig);
            var spe = pw.search(/[`~!@@#$%^&*|₩₩₩'₩";:₩/?]/gi);

            if(pw.length < 8 || pw.length > 20){

            alert("8자리 ~ 20자리 이내로 입력해주세요.");
            return false;
            }else if(pw.search(/\s/) != -1){
            alert("비밀번호는 공백 없이 입력해주세요.");
            return false;
            }else if(num < 0 || eng < 0 || spe < 0 ){
            alert("영문,숫자, 특수문자를 혼합하여 입력해주세요.");
            return false;
            }else {
            console.log("통과"); 
            return true;
            }
        };

        if(u_pwd.value != re_pwd.value){
            alert("비밀번호를 확인해주세요.");
            re_pwd.focus();
            return false;
        };

        if(!agree.checked){
            alert("약관 동의가 필요합니다.");
            agree.focus();
            return false;
        };

        return true;
    };

    function select_email(){
            var email_dns = document.getElementById("email_dns");
            var email_sel = document.getElementById("email_sel");

            var idx = email_sel.options.selectedIndex;
            var rtn_val = email_sel.options[idx].value;

            email_dns.value = rtn_val;
        };

